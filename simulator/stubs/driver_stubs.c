char* sdcard_read_file(const char* path, int* out_len) {
    extern char g_base_path[512];
    char full_path[1024];
    
    if (path[0] == '/') {
        snprintf(full_path, sizeof(full_path), "%s%s", g_base_path, path);
    } else {
        snprintf(full_path, sizeof(full_path), "%s/%s", g_base_path, path);
    }
    
    FILE* f = fopen(full_path, "rb");
    if (!f) {
        if (out_len) *out_len = 0;
        return NULL;
    }
    
    fseek(f, 0, SEEK_END);
    long size = ftell(f);
    fseek(f, 0, SEEK_SET);
    
    char* buf = (char*)malloc(size + 1);
    if (!buf) {
        fclose(f);
        if (out_len) *out_len = 0;
        return NULL;
    }
    
    fread(buf, 1, size, f);
    buf[size] = '\0';
    fclose(f);
    
    if (out_len) *out_len = (int)size;
    return buf;
}
// Define sdcard_entry_t locally to avoid including pico headers
typedef struct {
    char     name[256];
    bool     is_dir;
    uint32_t size;
    uint16_t fdate;
    uint16_t ftime;
} sdcard_entry_t;

int sdcard_list_dir(const char* path,
                    void (*callback)(const sdcard_entry_t* entry, void* user),
                    void* user) {
    // Real implementation using host filesystem
    // g_base_path is defined in hal_sdcard.c
    extern char g_base_path[512];
    const char* base = g_base_path;
    
    char full_path[1024];
    // The base path is the location of the SD card contents
    // PicOS paths like /apps should map directly to the filesystem
    if (strcmp(path, "/") == 0) {
        // Root maps to base
        strncpy(full_path, base, sizeof(full_path) - 1);
        full_path[sizeof(full_path) - 1] = '\0';
    } else if (path[0] == '/') {
        // Other paths: append to base (skip leading /)
        snprintf(full_path, sizeof(full_path), "%s%s", base, path);
    } else {
        snprintf(full_path, sizeof(full_path), "%s/%s", base, path);
    }
    
    printf("[SDCARD] Listing directory: %s (full: %s)\n", path, full_path);
    fflush(stdout);
    
    DIR* dir = opendir(full_path);
    if (!dir) {
        printf("[SDCARD] Failed to open directory: %s\n", full_path);
        fflush(stdout);
        return -1;
    }
    
    int count = 0;
    struct dirent* entry;
    while ((entry = readdir(dir)) != NULL) {
        // Skip dotfiles (FAT32 doesn't have them; filters .DS_Store, .git, etc.)
        if (entry->d_name[0] == '.') {
            continue;
        }
        
        // Build sdcard_entry_t
        sdcard_entry_t sdc_entry;
        strncpy(sdc_entry.name, entry->d_name, sizeof(sdc_entry.name) - 1);
        sdc_entry.name[sizeof(sdc_entry.name) - 1] = '\0';
        
        char entry_path[1024];
        snprintf(entry_path, sizeof(entry_path), "%s/%s", full_path, entry->d_name);
        
        struct stat st;
        if (stat(entry_path, &st) == 0) {
            sdc_entry.is_dir = S_ISDIR(st.st_mode);
            sdc_entry.size = (uint32_t)st.st_size;
            // Set default date/time (Jan 1, 2020)
            sdc_entry.fdate = (2020 - 1980) << 9 | 1 << 5 | 1;
            sdc_entry.ftime = 0;
        } else {
            sdc_entry.is_dir = false;
            sdc_entry.size = 0;
            sdc_entry.fdate = 0;
            sdc_entry.ftime = 0;
        }
        
        printf("[SDCARD] Found: %s (is_dir=%d)\n", sdc_entry.name, sdc_entry.is_dir);
        fflush(stdout);
        
        // Call the callback
        if (callback) {
            callback(&sdc_entry, user);
        }
        count++;
    }
    
    closedir(dir);
    printf("[SDCARD] Listed %d entries\n", count);
    fflush(stdout);
    return count;
}

