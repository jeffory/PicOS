// Active font tracking
static int s_active_font = 0;  // 0 = 6x8, 1 = 8x12, 2 = scientifica, 3 = bold

int display_draw_text(int x, int y, const char* text, uint16_t fg, uint16_t bg) {
    int start_x = x;
    uint16_t* fb = display_get_back_buffer();
    
    if (s_active_font == 1) {
        // 8x12 font: row-major, MSB = leftmost pixel
        while (*text) {
            char c = *text++;
            if (c < 0x20 || c > 0x7E) c = '?';
            const uint8_t *glyph = s_font8x12[c - 0x20];
            for (int row = 0; row < FONT8X12_H; row++) {
                uint8_t rowdata = glyph[row];
                int py = y + row;
                if (py < 0 || py >= 320) continue;
                for (int col = 0; col < FONT8X12_W; col++) {
                    int px = x + col;
                    if (px >= 0 && px < 320) {
                        fb[py * 320 + px] = (rowdata & (0x80 >> col)) ? fg : bg;
                    }
                }
            }
            x += FONT8X12_W;
        }
    } else if (s_active_font == 2 || s_active_font == 3) {
        // Scientifica fonts: row-major, MSB = leftmost pixel, 6x12
        while (*text) {
            char c = *text++;
            if (c < 0x20 || c > 0x7E) c = '?';
            const uint8_t *glyph = (s_active_font == 3)
                ? font_scientifica_bold[c - 0x20]
                : font_scientifica[c - 0x20];
            for (int row = 0; row < FONT_SCI_HEIGHT; row++) {
                uint8_t rowdata = glyph[row];
                int py = y + row;
                if (py < 0 || py >= 320) continue;
                for (int col = 0; col < FONT_SCI_WIDTH; col++) {
                    int px = x + col;
                    if (px >= 0 && px < 320) {
                        fb[py * 320 + px] = (rowdata & (0x80 >> col)) ? fg : bg;
                    }
                }
            }
            x += FONT_SCI_WIDTH;
        }
    } else {
        // 6x8 font: column-major, LSB = top pixel
        while (*text) {
            char c = *text++;
            if (c < 0x20 || c > 0x7E) c = '?';
            const uint8_t *glyph = s_font6x8[c - 0x20];
            for (int col = 0; col < FONT_W; col++) {
                uint8_t coldata = glyph[col];
                for (int row = 0; row < FONT_H; row++) {
                    int px = x + col;
                    int py = y + row;
                    if (px >= 0 && px < 320 && py >= 0 && py < 320) {
                        fb[py * 320 + px] = (coldata & (1 << row)) ? fg : bg;
                    }
                }
            }
            x += FONT_W;
        }
    }
    return x - start_x;
}

