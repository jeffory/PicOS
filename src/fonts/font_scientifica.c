const uint8_t* font_scientifica_glyph(char c) {
    uint8_t uc = (uint8_t)c;
    if (uc >= FONT_SCI_EXTENDED_START && uc < FONT_SCI_EXTENDED_START + FONT_SCI_EXTENDED_COUNT)
        return font_scientifica_extended[uc - FONT_SCI_EXTENDED_START];
    if (uc < 0x20 || uc > 0x7E) return font_scientifica[0];
    return font_scientifica[uc - 0x20];
}

