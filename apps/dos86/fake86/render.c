/*
  DOS86 — VRAM-to-RGB565 renderer.
  Converts emulated PC video memory to a 320x200 RGB565 output buffer
  based on the current video mode.

  Supported modes:
    0x02, 0x03  — 80x25 text mode (4px-wide glyphs for 320px width)
    0x04, 0x05  — CGA 320x200 4-color
    0x06        — CGA 640x200 2-color (halved to 320px)
    0x13        — VGA 320x200 256-color (Mode 13h)
*/

