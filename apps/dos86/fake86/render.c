/* ---- Text mode renderer (modes 0x02, 0x03) ----
   80x25, 2 bytes per cell (char, attr).
   320px / 80 cols = 4px per character.
   We sample columns 0,2,4,6 from the 8x8 glyph to get 4px width.
   200px / 25 rows = 8px per row. */

static void render_text_mode(uint16_t *out) {
    for (int row = 0; row < 25; row++) {
        for (int scanline = 0; scanline < 8; scanline++) {
            int y = row * 8 + scanline;
            uint16_t *dst = &out[y * 320];

            for (int col = 0; col < 80; col++) {
                uint16_t cell_off = (uint16_t)(row * 80 + col) * 2;
                uint8_t ch   = g_vram[cell_off];
                uint8_t attr = g_vram[cell_off + 1];

                uint8_t fg_idx = attr & 0x0F;
                uint8_t bg_idx = (attr >> 4) & 0x07;
                uint16_t fg = ega_palette_rgb565[fg_idx];
                uint16_t bg = ega_palette_rgb565[bg_idx];

                /* Get font row for this character */
                uint8_t glyph_row = font8x8[ch & 0x7F][scanline];

                /* Sample columns 0,2,4,6 from the 8-pixel-wide glyph
                   to produce 4 pixels per character */
                dst[col * 4 + 0] = (glyph_row & 0x80) ? fg : bg;
                dst[col * 4 + 1] = (glyph_row & 0x20) ? fg : bg;
                dst[col * 4 + 2] = (glyph_row & 0x08) ? fg : bg;
                dst[col * 4 + 3] = (glyph_row & 0x02) ? fg : bg;
            }
        }
    }

    /* Render cursor (blinking block) — always show for now */
    if (video.cursor_pos < 80 * 25) {
        uint8_t ccol = (uint8_t)(video.cursor_pos % 80);
        uint8_t crow = (uint8_t)(video.cursor_pos / 80);
        uint8_t attr = g_vram[video.cursor_pos * 2 + 1];
        uint16_t fg = ega_palette_rgb565[attr & 0x0F];

        for (int sl = video.cursor_start; sl <= video.cursor_end && sl < 8; sl++) {
            int y = crow * 8 + sl;
            uint16_t *dst = &out[y * 320 + ccol * 4];
            dst[0] = fg;
            dst[1] = fg;
            dst[2] = fg;
            dst[3] = fg;
        }
    }
}

/* ---- CGA 320x200 4-color (modes 0x04, 0x05) ----
   2 bits per pixel. Even rows at VRAM offset 0, odd rows at 0x2000.
   Interleaved layout. */

static void render_cga_320(uint16_t *out) {
    /* Select palette based on overscan color register (simplified) */
    const uint16_t *pal = cga_pal1_hi;

    for (int y = 0; y < 200; y++) {
        uint16_t *dst = &out[y * 320];
        uint32_t base = (y & 1) ? 0x2000 : 0x0000;
        uint32_t row_off = base + (y >> 1) * 80;

        for (int x_byte = 0; x_byte < 80; x_byte++) {
            uint8_t b = g_vram[row_off + x_byte];
            /* 4 pixels per byte, 2 bits each, MSB first */
            dst[x_byte * 4 + 0] = pal[(b >> 6) & 3];
            dst[x_byte * 4 + 1] = pal[(b >> 4) & 3];
            dst[x_byte * 4 + 2] = pal[(b >> 2) & 3];
            dst[x_byte * 4 + 3] = pal[ b       & 3];
        }
    }
}

/* ---- CGA 640x200 2-color (mode 0x06) ----
   1 bit per pixel, same interleave. Scale 2:1 horizontally to 320px. */

static void render_cga_640(uint16_t *out) {
    for (int y = 0; y < 200; y++) {
        uint16_t *dst = &out[y * 320];
        uint32_t base = (y & 1) ? 0x2000 : 0x0000;
        uint32_t row_off = base + (y >> 1) * 80;

        for (int x_byte = 0; x_byte < 80; x_byte++) {
            uint8_t b = g_vram[row_off + x_byte];
            /* 8 pixels per byte, but we output 4 (every other pair merged) */
            dst[x_byte * 4 + 0] = (b & 0x80) ? 0xFFFF : 0x0000;
            dst[x_byte * 4 + 1] = (b & 0x20) ? 0xFFFF : 0x0000;
            dst[x_byte * 4 + 2] = (b & 0x08) ? 0xFFFF : 0x0000;
            dst[x_byte * 4 + 3] = (b & 0x02) ? 0xFFFF : 0x0000;
        }
    }
}

/* ---- VGA Mode 13h (320x200 256-color) ----
   Linear, 1 byte per pixel = palette index.
   Look up RGB565 from the VGA DAC palette. */

static void render_mode13h(uint16_t *out) {
    for (int y = 0; y < 200; y++) {
        uint16_t *dst = &out[y * 320];
        const uint8_t *src = &g_vram[y * 320];

        for (int x = 0; x < 320; x++) {
            uint8_t idx = src[x];
            dst[x] = dac_to_rgb565(video.dac[idx * 3 + 0],
                                    video.dac[idx * 3 + 1],
                                    video.dac[idx * 3 + 2]);
        }
    }
}

/* ---- Public API ---- */

bool render_frame(uint16_t *out_buf) {
    switch (video.video_mode) {
        case 0x00:
        case 0x01:
        case 0x02:
        case 0x03:
            render_text_mode(out_buf);
            return true;

        case 0x04:
        case 0x05:
            render_cga_320(out_buf);
            return true;

        case 0x06:
            render_cga_640(out_buf);
            return true;

        case 0x13:
            render_mode13h(out_buf);
            return true;

        default:
            /* Unsupported mode — clear to black */
            memset(out_buf, 0, 320 * 200 * sizeof(uint16_t));
            return false;
    }
}

int render_get_width(void) {
    return 320;
}

int render_get_height(void) {
    return 200;
}
