/* ---- DAC 6-bit to RGB565 conversion ---- */
static inline uint16_t dac_to_rgb565(uint8_t r, uint8_t g, uint8_t b) {
    /* DAC values are 6-bit (0-63), scale to RGB565 */
    return ((uint16_t)(r >> 1) << 11) | ((uint16_t)g << 5) | (uint16_t)(b >> 1);
}

