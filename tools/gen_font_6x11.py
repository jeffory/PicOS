def generate_c_header():
    output = []
    output.append("// 6x11 terminal font - derived from s_font6x8 (display.c)")
    output.append("// Each column shifted left 2 bits to center 7-row glyph in 11-row cell")
    output.append("// Rows 0-1: top padding, Rows 2-8: glyph data, Rows 9-10: bottom padding")
    output.append("")
    output.append("#include \"font_6x11.h\"")
    output.append("#include <stddef.h>")
    output.append("")
    output.append("const uint16_t font_6x11[FONT_6X11_COUNT][FONT_6X11_WIDTH] = {")

    for i in range(95):
        char_code = 0x20 + i
        char = chr(char_code)
        glyph_6x8 = FONT_6X8[i]

        cols = []
        for col in range(6):
            # Shift 7-bit column data left by 2 to center in 11-bit field
            val = (glyph_6x8[col] & 0x7F) << 2
            cols.append(f"0x{val:03x}")

        # Escape special chars for comment
        display_char = char
        if char == '\\':
            display_char = '\\\\'
        elif char == "'":
            display_char = "\\'"

        output.append(f"    {{{', '.join(cols)}}},  // '{display_char}' (0x{char_code:02x})")

    output.append("};")
    output.append("")
    output.append("int font_6x11_width(char c) {")
    output.append("    if (c < FONT_6X11_START || c > FONT_6X11_END) return 0;")
    output.append("    return FONT_6X11_WIDTH;")
    output.append("}")
    output.append("")
    output.append("const uint16_t* font_6x11_glyph(char c) {")
    output.append("    if (c < FONT_6X11_START || c > FONT_6X11_END) return NULL;")
    output.append("    return font_6x11[c - FONT_6X11_START];")
    output.append("}")

    return "\n".join(output)


if __name__ == "__main__":
    print(generate_c_header())
